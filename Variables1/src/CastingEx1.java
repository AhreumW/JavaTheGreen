
public class CastingEx1 {
	
	public static void main(String[] args) {
		
		double d = 100.0;
		int num = 100;
		
		double sum = d + num;
		int sum2 = (int)d + num;
		
		System.out.println("d = " +d);
		System.out.println("num = " +num);
		System.out.println("sum = " +sum+ " sum2 = " +sum2);
		
	}

}
